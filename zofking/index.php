                                                <h4>oyuncu etkileşimi</h4>
                                                <p><strong>Enter</strong> tuşuna basmak, sohbet penceresini açar. Bu pencereye yazdığın mesajlar, oyundaki diğer sihirdarlara gider. Takıma Gönder'e tıklarsan, yazdıklarını sadece takım arkadaşların görür, rakipler görmez. Herkese Gönder'e tıklarsan, Seçeneklerden Herkese Açık sohbeti seçmiş olan herkes mesajını görür. Oyun, <strong>Enter</strong>'a yine bastığında mesajını sadece takımının göreceği şekilde ayarlanmıştır. Sohbet penceresi boşsa, <strong>Enter</strong>'a basarak kapatabilirsin.</p>
                                                <hr>
                                                <h4>Özel mesajlar</h4>
                                                <p>Özel mesaj ya da fısıltı, tek bir sihirdarın göreceği bir mesajdır. Önce<strong>/whisper</strong> ya da <strong>/w</strong>, ardından gideceği sihirdarın adını, son olarak da yollamak istediğin mesajı yazıp gönder.</p>
                                                <p><em>/whisper SteveMcQueen dokuz kuyruklu tilki, tembel yordle'ın üzerinden atladı.</em></p>
                                                <p>Sohbet konsoluna <strong>/reply</strong> veya <strong>/r</strong> yazmak, sana son fısıldayan son sihirdara otomatik olarak ileti yollamanı sağlar. Bir sihirdarın senden özel iletiler alması için arkadaş listende bulunması gerekir.</p>
                                                <hr>
                                                <h4>Oyuncuları susturmak veya yok saymak</h4>
                                                <p>Bazen, bir sihirdarın gönderdiği mesajları görmek istemeyebilirsin. Onu susturmak için <strong>Tab</strong> tuşuna basarak skor tablosunu aç ve sihirdarın adının yanındaki susturma düğmesine bas.</p>
                                                <p>Veya, <strong>/mute</strong> yazdıktan sonra uygunsuz davranan oyuncunun sihirdar adını gir. Bu işlemi tekrarlamak, oyuncunun mesajlarının tekrar görünür olmasını sağlar.</p>
